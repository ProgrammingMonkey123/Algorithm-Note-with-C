#include <stdio.h>

int main(){
    int i,j,k,l;
    for (i=1;i<=5;i++){
        printf("%3d",i);
    }
    printf("\n");
    for (j=1;j<=5;j++){
        printf("%3d",2 * j);
    }
    printf("\n");
    for (k=1;k<=5;k++){
        printf("%3d",3 * k);
    }
    printf("\n");
    for (l=1;l<=5;l++){
        printf("%3d",4 * l);
    }
    return 0;
}
